<script>
document.addEventListener("DOMContentLoaded", function () {

    /* MENÚ HAMBURGUESA */
    const menuToggle = document.getElementById("menu-toggle");
    const mainNav = document.getElementById("main-nav");

    if (menuToggle && mainNav) {
        menuToggle.addEventListener("click", function () {
            mainNav.classList.toggle("active");
        });
    }

    /* ANIMACIONES AL HACER SCROLL */
    const elementosAnimados = document.querySelectorAll(".reveal");

    const observer = new IntersectionObserver((entradas) => {
        entradas.forEach((entrada) => {
            if (entrada.isIntersecting) {
                entrada.target.classList.add("is-visible");
            }
        });
    }, {
        threshold: 0.18,
        rootMargin: "0px 0px -60px 0px"
    });

    elementosAnimados.forEach((elemento) => {
        observer.observe(elemento);
    });

    /* CARRUSELES DE INSTALACIONES */
    const carousels = document.querySelectorAll("[data-carousel]");

    carousels.forEach((carousel) => {
        const track = carousel.querySelector(".inst-carousel-track");
        const slides = carousel.querySelectorAll("img");
        const prevBtn = carousel.querySelector("[data-prev]");
        const nextBtn = carousel.querySelector("[data-next]");
        const dotsContainer = carousel.querySelector("[data-dots]");

        let index = 0;

        slides.forEach((_, i) => {
            const dot = document.createElement("button");
            dot.type = "button";
            dot.setAttribute("aria-label", "Ver imagen " + (i + 1));

            if (i === 0) {
                dot.classList.add("active");
            }

            dot.addEventListener("click", () => {
                index = i;
                updateCarousel();
            });

            dotsContainer.appendChild(dot);
        });

        const dots = dotsContainer.querySelectorAll("button");

        function updateCarousel() {
            track.style.transform = "translateX(-" + index * 100 + "%)";

            dots.forEach((dot) => dot.classList.remove("active"));
            dots[index].classList.add("active");
        }

        nextBtn.addEventListener("click", () => {
            index = (index + 1) % slides.length;
            updateCarousel();
        });

        prevBtn.addEventListener("click", () => {
            index = (index - 1 + slides.length) % slides.length;
            updateCarousel();
        });
    });

});
</script>

